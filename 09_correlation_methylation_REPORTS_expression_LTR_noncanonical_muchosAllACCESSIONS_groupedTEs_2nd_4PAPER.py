def calculate_correlation_between_matrices(matrix1, matrix2):
    """
    Calculates the correlation between the columns of two matrices.
    Args:
        matrix1: The first matrix (numpy array).
        matrix2: The second matrix (numpy array).
    Returns:
        A numpy array containing the correlation coefficients between each column of matrix1 and each column of matrix2.
    """
    num_cols1 = matrix1.shape[1]
    num_cols2 = matrix2.shape[1];#print (num_cols1,num_cols2)
    correlation_matrix = np.zeros((num_cols1, num_cols2));#print (matrix1)
    for i in range(num_cols1):
        for j in range(num_cols2):
            col1 = matrix1.iloc[:, i];#; print (col1)
            col2 = matrix2.iloc[:, j]#; print (col2)
            correlation_matrix[i, j] = np.ma.corrcoef(np.ma.masked_invalid(col1), np.ma.masked_invalid(col2))[0, 1]
    return correlation_matrix
