def Plot_scatter (TEs_subgroup,guide,context,scatter,ymin,ymax): # y is the maximun y-axis
    plt.clf();plt.cla() # clear previous plot and axes 
    if guide == "INNER": guide = "In-between LTRs" # change name in plots
    Label = "m{} {} for {}".format(context,guide,TEs_subgroup)
    x,y = zip(*scatter)
    x = np.array(x, dtype=float)
    y = np.array(y, dtype=float)
    
    # Create a mask for non-NaN values in both arrays
    mask = ~np.isnan(x) & ~np.isnan(y)
    # Filter the arrays
    x_filtered = x[mask]
    y_filtered = y[mask]
    if len(x_filtered) > 10: # Ensure there's enough data for correlation
        pearson_corr, P_p_value = pearsonr(x_filtered, y_filtered)
        spearman_corr,S_p_value = spearmanr(x_filtered, y_filtered)
        print("Pearson   Correlation: {}\t{}\{}".format(Label,pearson_corr,P_p_value))
        print("Spearmann Correlation: {}\t{}\{}".format(Label,spearman_corr,S_p_value))
        
        # for linear regression
        slope, intercept, r_value, p_value, std_err = linregress(x_filtered, y_filtered)
        # 1. Calculate the predicted Y values (y_hat) for the linear model
        y_linear_predicted = slope * x_filtered + intercept
        # 2. Calculate the residuals (errors)
        linear_residuals = y_filtered - y_linear_predicted
        # 3. Calculate the Sum of Squared Errors (SSE)
        sse_linear = np.sum(linear_residuals**2)
        # 4. Get the number of observations (n)
        n = len(y_filtered) 
        # Note: Use len(y_filtered) for consistency with the polynomial RMSE
        # 5. Calculate the Root Mean Square Error (RMSE)
        linear_rmse = np.sqrt(sse_linear / n)
        print("Lineregress: {}\tR2 {}\Pval {}\RMSE {}\slope {}".format(Label,r_value**2,p_value,linear_rmse,slope))
        
        ## for the polynomial
        # Calculate the mean of x_filtered to use for centering, and the centered x-variable 
        mean_x = np.mean(x_filtered)
        x_centered_vector = x_filtered - mean_x # x_centered_vector is a vector (N rows)
        # Create the polynomial design matrix (X)
        # For a third-order polynomial, we need x_centered, x_centered^2, and x_centered^3
        X_poly = np.column_stack([x_centered_vector, x_centered_vector**2, x_centered_vector**3]) # , x_centered_vector**4
        # Add a constant (intercept) term for statsmodels OLS
        X_poly = sm.add_constant(X_poly, prepend=True) # prepend=True is default but good to specify
        # Now, X_poly will have N rows and 4 columns (constant, x', x'^2, x'^3) And y_filtered (endog) has N rows.
        # Fit the OLS model
        model = sm.OLS(y_filtered, X_poly)
        results = model.fit()
        # print the model summary to get R-squared and p-values
        #print(model.summary())
        # You can also access specific values directly:
        POLYr_squared = results.rsquared
        POLY_p_val = results.f_pvalue # Overall model significance
        POLYp_values  = results.pvalues[1:] # Exclude p-value for the constant term
        # for plotting, create a smooth range for plotting
        # 1. Create a smooth x-range (e.g., 200 points)
        x_smooth = np.linspace(x_filtered.min(), x_filtered.max(), 200)
        # 2. Center the new x-range using the *original* mean_x
        x_smooth_centered = x_smooth - mean_x 
        # 3. Create the polynomial design matrix for the smooth range
        # This must mirror the original design matrix (X_poly) exactly!
        X_smooth_poly = np.column_stack([x_smooth_centered, x_smooth_centered**2, x_smooth_centered**3]) #, x_smooth_centered**4
        X_smooth_poly = sm.add_constant(X_smooth_poly, prepend=True)
        # 4. Calculate predictions for the smooth range and plot the smooth line
        y_smooth_pred = results.predict(X_smooth_poly)
        # Calculate the squared differences (Step 1 & 2)
        # 1. Get the residuals (actual y - predicted y) for the filtered data
        residuals = results.resid
        # 2. Calculate the Sum of Squared Errors (SSE)
        sse = np.sum(residuals**2)
        # 3. Get the number of observations (n)
        # Note: Use len(y_filtered) which is the actual number of data points used for fitting
        # 4. Calculate the Root Mean Square Error (RMSE)
        # RMSE = sqrt(SSE / n)
        polynomial_rmse = np.sqrt(sse / n)
        print("Centered 3 Polynomial: {}\tR2 {}\Pval {} RMSE {}".format(Label,POLYr_squared,POLYp_values,polynomial_rmse))
    else:
        print("Not enough data after removing NaNs to calculate correlation.")
    
    ### Plot
    f, ax = plt.subplots()
    f = plt.figure(figsize=(12,8))
    ax = f.add_axes((0.10, 0.125, 0.80, 0.85)) # left, bottom, width, height in normalized units  
    plt.xticks(fontname = 'arial',fontsize=20,fontweight = 700) 
    plt.yticks(fontname = 'arial',fontsize=20,fontweight = 700)
    plt.ylabel('{}'.format(Label),fontname = 'arial', fontsize=24,fontweight = 700, labelpad= 1.0)
    plt.xlabel('Transcript level (Log2 normalized cpm)',fontname = 'arial', fontsize=24,fontweight = 700, labelpad=24.0)
    #plt.title(r'Histogram')
    plt.subplots_adjust(left=0.10)     
    if context == "CG": plt.scatter(x_filtered, y_filtered,color='red',rasterized=True)  
    if context == "CHG": plt.scatter(x_filtered, y_filtered,color='blue',rasterized=True) 
    if context == "CHH": plt.scatter(x_filtered, y_filtered,color='green',rasterized=True) 
    
    plt.ylim(ymin,ymax)
    left, right = plt.xlim()
    plt.xlim(-2.5,12.5)   

    if len(x_filtered) > 10:
        # Create the complex label for the legend
        # .3f = 3 decimal places, .2e = scientific notation
        legend_label = (f"Polynomial fit:\n"
                        f"$R^2$ = {POLYr_squared:.3f}\n"
                        f"$p$ = {POLY_p_val:.2e}\n"
                        f"RMSE = {polynomial_rmse:.3f}")
        
        ax.plot(x_smooth, y_smooth_pred, color='black', linewidth=4.0, label=legend_label)
        ax.legend(fontsize=18, loc='upper right', frameon=True)
    plt.savefig("{}{}.png".format(path_methyl,Filegraph),dpi=300) 
    plt.savefig("{}{}.svg".format(path_methyl,Filegraph), format="SVG", dpi=600, bbox_inches='tight')
    plt.close()
