def Plot_violin_box (data, y_column, title, y_label, path_methyl, file_suffix, plot_type='violin', pvalues=None, y_max_limit=None, y_min_limit=None, box_color=None): 
    """
    Final function incorporating scale consistency, color, margin, and clipping fixes.
    """
    plt.clf(); plt.cla() 
    
    # --- 1. Plot Dimensions and Box Width (Set by User) ---
    FIGURE_WIDTH  = 5.0 
    FIGURE_HEIGHT = 12.0
    BOX_WIDTH     = 0.4
    BOX_LINEWIDTH = 2.0 
    
    # Properties for the outlier dots (fliers)
    FLIER_PROPS = dict(marker='o', markersize=1, markerfacecolor='blue', markeredgecolor='blue') 
    # Define line properties for whiskers/caps/median to be black ('k')
    LINE_PROPS = dict(color='k', linewidth=BOX_LINEWIDTH) 
    # Use 'boxprops' to set 'edgecolor' when using 'palette'.
    BOX_PROPS_FIXED = dict(edgecolor='k', linewidth=BOX_LINEWIDTH)
    f, ax = plt.subplots(figsize=(FIGURE_WIDTH, FIGURE_HEIGHT)) 
    # 1. Create the plot (Seaborn part)
    if box_color:
        num_groups = len(data['TE_Subgroup'].unique())
        palette = [box_color] * num_groups
    else:
        palette = "Set2"
    if plot_type == 'violin':
        sns.violinplot(x='TE_Subgroup', y=y_column, data=data, ax=ax, inner="quartile", palette=palette, legend=False, hue='TE_Subgroup')
    elif plot_type == 'box':
        # FIX: Removed incorrect direct 'edgecolor' parameter, replaced with 'boxprops'
        sns.boxplot(
                x='TE_Subgroup', 
                y=y_column, 
                data=data, 
                ax=ax, 
                palette=palette, 
                legend=False, 
                hue='TE_Subgroup', 
                width=BOX_WIDTH,
                linewidth=BOX_LINEWIDTH, 
                flierprops=FLIER_PROPS,
                # --- COLOR FIX FOR BORDERS AND FILL ---
                boxprops=BOX_PROPS_FIXED, 
                # These remain to force all other lines to black
                capprops=LINE_PROPS,      
                whiskerprops=LINE_PROPS,  
                medianprops=dict(color='black', linewidth=BOX_LINEWIDTH)
        )
    ymin_initial, ymax_initial = ax.get_ylim()
    
    # 2. --- P-VALUE ANNOTATION ---
    if pvalues:
        comparison_pairs = list(pvalues.keys())
        def p_to_stars(p):
            if p < 0.0001: return "****"
            if p < 0.001: return "***"
            if p < 0.01: return "**"
            if p < 0.05: return "*"
            return "ns"
        
        numeric_pvalues_for_plot = [pvalues[pair] for pair in comparison_pairs]
        annotator = Annotator(ax, comparison_pairs, data=data, x='TE_Subgroup', y=y_column)
        annotator.configure(test=None, loc='inside', line_offset=0.1, text_format='star', verbose=0)
        annotator.set_pvalues(numeric_pvalues_for_plot)
        annotator.annotate() 

    # 3. --- Absolute Y-Axis Scale Enforcement (FIXED SCALE) ---
    ymin_annotated, ymax_annotated = ax.get_ylim()
    if y_max_limit is not None and y_min_limit is not None:
        tick_max = y_max_limit
        tick_min = y_min_limit
    else:
        tick_max = ymax_annotated
        tick_min = ymin_annotated
    
    # Standardize the space reserved for annotations for methylation plots
    if y_column == 'Methylation' and np.isclose(tick_max, 1.0):
        # Increased to 0.4 to accommodate the largest mCG annotation bar without clipping.
        FIXED_ANNOTATION_SPACE = 0.4 
        final_plot_ymax = tick_max + FIXED_ANNOTATION_SPACE 
    else:
        # Use dynamic space for other plots (e.g., Expression)
        annotation_space_needed = ymax_annotated - ymax_initial 
        final_plot_ymax = tick_max + annotation_space_needed 
    if np.isclose(tick_min, 0.0):
        final_plot_ymin = -0.05 
    else:
        final_plot_ymin = tick_min
    ax.set_ylim(final_plot_ymin, final_plot_ymax)
    
    # 4. --- Tick Generation and Formatting ---
    visible_tick_range = tick_max - tick_min
    if visible_tick_range <= 1.5 and np.isclose(tick_max, 1.0):
        TICK_INTERVAL = 0.2
    elif visible_tick_range > 1.5 and visible_tick_range <= 10:
        TICK_INTERVAL = 1.0
    else:
        TICK_INTERVAL = 2.5 
    start = np.floor(tick_min / TICK_INTERVAL) * TICK_INTERVAL
    end = np.ceil(tick_max / TICK_INTERVAL) * TICK_INTERVAL
    
    generated_ticks = np.arange(start, end + TICK_INTERVAL / 2, TICK_INTERVAL)
    new_ticks = []
    for t in generated_ticks:
        if t > tick_max + 1e-9:
            continue
        if np.isclose(tick_min, 0.0) and t < -1e-9:
             continue
        new_ticks.append(t)
    
    if y_max_limit is not None and not any(np.isclose(t, y_max_limit) for t in new_ticks):
        new_ticks.append(y_max_limit)
    if y_min_limit is not None and not any(np.isclose(t, y_min_limit) for t in new_ticks):
        new_ticks.append(y_min_limit)
    
    final_ticks = sorted([round(t, 5) for t in list(set(new_ticks))])
    ax.set_yticks(final_ticks)
    
    # Formatting
    ax.set_title(title, fontname='arial', fontsize=20, fontweight=700)
    ax.set_xlabel('', fontname='arial', fontsize=24, fontweight=700) # labelpad=24.0)
    ax.set_ylabel(y_label, fontname='arial', fontsize=24, fontweight=700, labelpad=10.0)
    
    plt.xticks(fontname='arial', fontsize=20, fontweight=700, rotation=45, ha='right') 
    plt.yticks(fontname='arial', fontsize=20, fontweight=700)
    
    # Manually set margins. Increased 'left' to 0.3 to prevent y-axis label clipping.
    plt.subplots_adjust(left=0.30, right=0.95, top=0.95, bottom=0.20)
    
    # Save the plot
    Filegraph = f"{y_label.replace(' ', '_')}_{file_suffix}"
    plt.savefig(f"{path_methyl}{Filegraph}.png", dpi=300)
    plt.savefig(f"{path_methyl}{Filegraph}.svg", format="SVG", dpi=2000)
    plt.close()
