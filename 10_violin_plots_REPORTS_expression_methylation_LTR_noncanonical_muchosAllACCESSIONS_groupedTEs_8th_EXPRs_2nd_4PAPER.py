def calculate_group_pvalues(df, value_column):
    """
    Performs Kruskal-Wallis and post-hoc Dunn's tests on the data.
    Args:
        df (pd.DataFrame): The long-format DataFrame (Expression or Methylation).
        value_column (str): 'Expression' or 'Methylation'.
    Returns:
        dict: p-values for all pairwise comparisons.
    """
    # 1. Prepare data for Kruskal-Wallis
    group_names = df['TE_Subgroup'].unique()
    group_data = [df[df['TE_Subgroup'] == group][value_column].dropna() for group in group_names]
    # Check if there's enough data (more than 1 group and enough observations)
    valid_groups = [data for data in group_data if len(data) >= 5] 
    if len(valid_groups) < 2:
        print("Not enough valid groups or observations to perform Kruskal-Wallis test.")
        return {}
    # 2. Kruskal-Wallis H Test
    H_stat, p_kw = stats.kruskal(*valid_groups)
    print(f"\nKruskal-Wallis Test on {value_column}: H={H_stat:.3f}, P-value={p_kw:.4e}")
    if p_kw < 0.05:
            print("Overall difference is statistically significant. Performing post-hoc Dunn's test.")           
            # 3. Post-hoc Dunn's Test
            # p_adjust='bonferroni'
            p_matrix = sp.posthoc_dunn(group_data, p_adjust='bonferroni')
            p_matrix.columns = group_names
            p_matrix.index = group_names
# Extract pairwise p-values into a dictionary
            pairwise_pvalues = {}
            print(f"\n--- Dunn's Post-hoc P-values for {value_column} (Bonferroni) ---") # <-- NEW HEADER
            for i in range(len(group_names)):
                for j in range(i + 1, len(group_names)):
                    group1 = group_names[i]
                    group2 = group_names[j]
                    p_val = p_matrix.loc[group1, group2]
                    print(f"{group1} vs {group2}: P = {p_val:.4e}") 
                    pairwise_pvalues[(group1, group2)] = p_val      
            return pairwise_pvalues
    else:
        print("Overall difference is NOT statistically significant. No post-hoc test needed.")
        return {}
